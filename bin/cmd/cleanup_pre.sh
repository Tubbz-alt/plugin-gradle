CMD_DESCRIPTION="Cleanup all caches generated by the plugin."

function _remove_volume_if_exists()
{
	athena.info "Removing cached volume '$1'..."
	if athena.docker.volume_exists "$1"; then
		athena.docker volume rm "$1" >/dev/null
	fi
}

athena.info "Removing all caches..."
_remove_volume_if_exists "athena_cache_android"
_remove_volume_if_exists "athena_cache_gradle_project"
_remove_volume_if_exists "athena_cache_gradle_home"
